contract = web3.eth.contract(address=contract_address, abi=contract_abi)

def upload_to_ipfs(file_path):
    try:
        with open(file_path, 'rb') as file:
            response = requests.post(pinata_url, 
                                     files={'file': file},
                                     headers={
                                         'pinata_api_key': pinata_api_key,
                                         'pinata_secret_api_key': pinata_secret_api_key
                                     })
        res = response.json()
        ipfs_hash = res['IpfsHash']
        print(f"File uploaded to IPFS with Hash: {ipfs_hash}")
        return ipfs_hash
    except Exception as e:
        print(f"Error uploading to IPFS: {e}")
        return None

def mint_nft(ipfs_hash):
    try:
        nonce = web3.eth.get_transaction_count(wallet_address)
        txn = contract.functions.mintNFT(wallet_address, ipfs_hash).build_transaction({
            'chainId': 2442,
            'gas': 300000,
            'gasPrice': web3.eth.gas_price,
            'nonce': nonce
        })
        signed_txn = web3.eth.account.sign_transaction(txn, private_key)
        tx_hash = web3.eth.send_raw_transaction(signed_txn.raw_transaction)
        print(f"Transaction sent with hash: {web3.to_hex(tx_hash)}")
        
        tx_receipt = web3.eth.wait_for_transaction_receipt(tx_hash)
        print(f"Transaction was mined in block {tx_receipt.blockNumber}")
        return tx_receipt
    except Exception as e:
        print(f"Error minting NFT: {e}")
        return None

def retrieve_and_save_image(ipfs_hash, save_path):
    try:
        ipfs_url = f"https://gateway.pinata.cloud/ipfs/{ipfs_hash}"
        response = requests.get(ipfs_url)
        if response.status_code == 200:
            with open(save_path, 'wb') as file:
                file.write(response.content)
            print(f"Image successfully downloaded and saved to: {save_path}")
            return True
        else:
            print(f"Failed to retrieve image. Status code: {response.status_code}")
            return False
    except Exception as e:
        print(f"Error retrieving image: {e}")
        return False

def protect_and_retrieve_file(file_path):
    ipfs_hash = upload_to_ipfs(file_path)
    if ipfs_hash:
        receipt = mint_nft(ipfs_hash)
        if receipt:
            print(f"NFT minted successfully. Transaction hash: {receipt.transactionHash.hex()}")
            return f"https://gateway.pinata.cloud/ipfs/{ipfs_hash}"
        else:
            print("Failed to mint NFT.")
    else:
        print("Failed to upload file to IPFS.")
